package com.example.lab7iweb.bean;

public class Bean {
    private int id;
    private String nombre;
    private int n_categoria;
    private int n_pelicula;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getN_categoria() {
        return n_categoria;
    }

    public void setN_categoria(int n_categoria) {
        this.n_categoria = n_categoria;
    }

    public int getN_pelicula() {
        return n_pelicula;
    }

    public void setN_pelicula(int n_pelicula) {
        this.n_pelicula = n_pelicula;
    }


}
